public class ArithmeticOperator {
    public static void main(String[] args) {
        int a = 50;
        int b = 80;

        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
       

    }
}
