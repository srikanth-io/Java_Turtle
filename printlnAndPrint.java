public class printlnAndPrint {
    public static void main(String[] args) {
      
    //   Println
        System.out.println("Hello ");
        System.out.println("World");

    // Print
        System.out.print("Hello");
        System.out.print(" World");

    // Printf
        int a = 10;
        float b = 20.8f;
        System.out.println(" ");
        System.out.printf("%d", a);
        System.out.println(" ");
        System.out.printf("%f", b);
    }
}
